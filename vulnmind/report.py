# Styles
# ---------------------------------------------------------------------------

def _build_styles() -> dict:
    """Build a dict of ReportLab ParagraphStyles for the report."""
    base = getSampleStyleSheet()

    return {
        "title": ParagraphStyle(
            "VTitle",
            parent=base["Title"],
            fontSize=28,
            textColor=VULNMIND_BLUE,
            spaceAfter=6,
        ),
        "subtitle": ParagraphStyle(
            "VSubtitle",
            parent=base["Normal"],
            fontSize=14,
            textColor=colors.grey,
            spaceAfter=12,
        ),
        "risk": ParagraphStyle(
            "VRisk",
            parent=base["Normal"],
            fontSize=22,
            textColor=VULNMIND_BLUE,
            fontName="Helvetica-Bold",
            alignment=1,  # center
        ),
        "meta": ParagraphStyle(
            "VMeta",
            parent=base["Normal"],
            fontSize=10,
            textColor=colors.grey,
            alignment=1,  # center
        ),
        "meta_field": ParagraphStyle(
            "VMetaField",
            parent=base["Normal"],
            fontSize=9,
            textColor=colors.grey,
            spaceAfter=2,
        ),
        "h1": ParagraphStyle(
            "VH1",
            parent=base["Heading1"],
            fontSize=14,
            textColor=VULNMIND_BLUE,
            spaceAfter=4,
        ),
        "h2": ParagraphStyle(
            "VH2",
            parent=base["Heading2"],
            fontSize=11,
            textColor=VULNMIND_BLUE,
            spaceBefore=6,
            spaceAfter=4,
        ),
        "body": ParagraphStyle(
            "VBody",
            parent=base["Normal"],
            fontSize=10,
            leading=14,
            spaceAfter=6,
        ),
        "code": ParagraphStyle(
            "VCode",
            parent=base["Code"],
            fontSize=8,
            fontName="Courier",
            backColor=LIGHT_GREY,
            leftIndent=6,
            spaceAfter=3,
        ),
        "evidence": ParagraphStyle(
            "VEvidence",
            parent=base["Code"],
            fontSize=7,
            fontName="Courier",
            backColor=LIGHT_GREY,
            leftIndent=6,
            textColor=colors.HexColor("#555555"),
        ),
        "table_body": ParagraphStyle(
            "VTableBody",
            parent=base["Normal"],
            fontSize=9,
        ),
    }
